package com.example.beautyOfProgram;

//定义一个类，存放数组中的最大值和最小值
public class MaxAndMin {
	private int max;
	private int min;

	public int getMax() {
		return max;
	}

	public void setMax(int max) {
		this.max = max;
	}

	public int getMin() {
		return min;
	}

	public void setMin(int min) {
		this.min = min;
	}

}
